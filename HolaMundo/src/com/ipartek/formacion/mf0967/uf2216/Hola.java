package com.ipartek.formacion.mf0967.uf2216;

public class Hola {

	public static void main(String[] args) {
		System.out.println("Hola a todos");
	}
}

//java Hola -> JRE hace: Hola.main(new String[] {})

//package -> La "agrupación" (paquete) de clases la definimos con package

//public -> La clase se puede acceder desde cualquier clase
//class -> Define una nueva clase
//HolaMundo -> Nombre que nos inventamos para la clase

//public -> Se permite llamar al método desde cualquier clase
//static -> Se va a llamar al método desde el nombre de la propia clase
//void -> La ejecución del método no devuelve ningún resultado (es como un procedimiento)
//main() -> Método que se busca desde el JRE cuando vamos a ejecutar un programa Java desde consola

//String[] -> Array de String
//args -> argumentos recibidos por consola

//System -> Clase de la API de java que representa el sistema operativo sobre el que se está ejecutando la aplicación (pertenece al paquete java.lang)
//out -> Variable estática, representa la salida de consola
//println() -> Método, manda una línea a la consola
